class Solution:
    def generateParenthesis(self, n: int) -> List[str]:
        def recur(left, right, n, s):
            if left == n and right == n:
                res.append(s)
                return
            if left < n:
                recur(left + 1, right, n, s + "(")
            if left > right:
                recur(left, right + 1, n, s + ")")

        res = []
        recur(0, 0, n, "")
        return res
